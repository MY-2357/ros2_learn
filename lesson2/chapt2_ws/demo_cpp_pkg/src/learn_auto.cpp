#include <iostream>

int main()
{
    auto x = 10;
    auto y=3.14;
    auto z='a';
    auto str = "Hello, world!";
    std::cout << "x = " << x << std::endl;
    std::cout << "y = " << y << std::endl;
    std::cout << "z = " << z << std::endl;
    std::cout << "str = " << str << std::endl;
    return 0;
}